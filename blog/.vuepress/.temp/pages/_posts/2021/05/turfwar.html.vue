<h3 id="人数アドバンテージ" tabindex="-1"><a class="header-anchor" href="#人数アドバンテージ" aria-hidden="true">#</a> 人数アドバンテージ</h3>
<p>ナワバリバトルで最も重要視されるのは人数アドベンテージです。</p>
<p>ナワバリバトルの勝利要素が全部で 100 あるとしたら 70 くらいは人数アドバンテージです。回線落ちで最初から仲間が一人いないとよっぽど実力差がない限りそれだけでもうほとんど勝てなくなります。ナワバリバトルとはそういうゲームです。</p>
<p>何故勝てないのかというのは、単純にこちらが常に一落ち状態にあり「戦う」にしても「塗り」にしても圧倒的に不利だからです。</p>
<p>つまり、勝つためには人数アドバンテージを作り出せば良いことになります。これは要するに相手をたくさんキルして人数有利をつくれば勝ちやすいということです。</p>
