<h3 id="試合終了-30-秒前" tabindex="-1"><a class="header-anchor" href="#試合終了-30-秒前" aria-hidden="true">#</a> 試合終了 30 秒前</h3>
<p>次に大事なのが試合終了のラスト 30 秒です。</p>
<p>それまでどれだけ勝っていてもこの 30 秒で相手に人数アドバンテージを取られると厳しいです。一回デスをするとリスポーン地点に復帰してから前線に戻るまでに 10 秒以上かかります。10 秒あれば塗りが強いブキなら 100pt 近く塗られてしまいます。</p>
<h3 id="塗りポイントの価値の違いを理解する" tabindex="-1"><a class="header-anchor" href="#塗りポイントの価値の違いを理解する" aria-hidden="true">#</a> 塗りポイントの価値の違いを理解する</h3>
<p>「ナワバリバトルは塗るゲームだから」といって自陣ばっかり塗っているプレイヤーがいる。正直、それではどれだけやっても上手くならないし勝てるようにならない。それで勝てているのは残りの三人が人数アドバンテージをつくっていてくれているからです。</p>
<p>「何故自陣を塗っていて勝てないのか」ということなのですが、それは塗りの価値が違うからです。</p>
<p>例えば、こちらのチームが負けているまだ塗られていない自陣を 10 秒かけて 100pt 塗った（その間、相手チームは棒立ちしていた）と仮定しましょう。すると、こちらのチームは塗り 100pt を獲得して相手チームとの差が 100 詰まります。</p>
<p>では、相手チームのインクをこちらのインクで塗り返した場合はどうでしょうか？こちらのチームは 100pt 塗りが増えて相手チームは 100pt 塗りが減ります。つまり、合計すると 200pt 差が詰まります。</p>
<p>塗られていないところを塗るのと、相手インクを塗り替えすのは価値が倍違ってくるということを覚えておきましょう。要するに、自陣ばっかり塗っていると価値が低い塗りばかりしていてダメなわけです。</p>
<h3 id="自陣はいつ塗るか" tabindex="-1"><a class="header-anchor" href="#自陣はいつ塗るか" aria-hidden="true">#</a> 自陣はいつ塗るか</h3>
<p>といっても、自陣を塗らないわけにはいきません。平均的に自陣の塗りは全体の 30%近くを占めます。</p>
<p>非常に大雑把な概算ですが塗りポイントの内訳はお互いの自陣で 60%、中央の共通部分で 40%というところです。</p>
<p>ナワバリバトルの仕様の性質上、過半数つまり 50%以上塗れば確実に勝てますが、塗り残しや簡単には塗れない場所などがあるので実際には 47%近く塗っていればだいたい勝てます。よって、ここでは塗りポイント 47%を目指すことにします。</p>
