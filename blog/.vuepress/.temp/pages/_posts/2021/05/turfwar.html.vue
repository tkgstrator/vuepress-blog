<p>仮に相手陣地を全く塗らなかったと仮定しましょう。このとき、自陣塗りが 95%達成できていれば自陣だけで全体の 28.5%の塗りが確保できます。ということは、共通部分で 18.5%塗れば目標の 47%に達します。</p>
<p>すると、共通部分は 46.25%だけ塗れば良いことになります。自陣塗りができていれば中央の競り合いは多少負けてしまっても大丈夫ということです。</p>
<p>これが先程の最後の 30 秒の問題と深く関わってきます。</p>
<p>自陣が十分塗れている状況で残り 30 秒で少しの人数不利であればイチかバチかでキルを狙うより（もちろん、高い確率でたおせそうな相手はたおすべき）も、中央は少し負けてもいいので安全を優先したほうが良いケースがあるということです。キルは勝利への一番の近道と言いましたが、引くもまた戦術なのです。</p>
<p>で、話を戻すと「いつ自陣を塗るか」という話です。さっき「最後は必ずしもキル重視ではない」といいましたが、この判断を下すためには「中央にいて中央の状況がわかっていなければいけない」ということがわかるでしょうか。</p>
<p>つまり「相手が自陣に侵入してきてもいない」「自陣にたくさん相手の塗りがあるわけでもない」のに意味もなく自陣に引きこもっているのは、この判断を下すための情報を捨てていることになります。そもそも、こちらが勝っている状況なら相手は逆転しようと中央に集まりますし、負けているなら相手陣地に切り込んでいかなければいけません。どちらにせよ、最後に無意味に自陣にいるのはダメだということです。</p>
<p>最後に無意味に自陣にいて自陣を塗るのがダメであれば、自陣は最初のうちにある程度塗っておかないとダメということです。</p>
<p>それは開幕直後でもいいですし、リスポーン直後でも構いません。ラスト 30 秒までにある程度の自陣塗りは終わらせておきましょう。</p>
<h3 id="二対一を心がける" tabindex="-1"><a class="header-anchor" href="#二対一を心がける" aria-hidden="true">#</a> 二対一を心がける</h3>
<p>さて、塗りの話は終わったのでキルの話に戻ります。</p>
<p>結局、キルしたチームが勝ちやすいのでキルする方法を覚えるのが一番手っ取り早いです。</p>
<p>とはいえ、いきなり「キルをたくさんとれ」と言うのはハードルが高いです。ここでは簡単にキル数を増やす方法を教えます。</p>
<p>それは相手と対面するときに「二対一を心がける」ということです。</p>
<p>どんなに上手いプレイヤーでも一度に同時に二方向を撃つことはできません。挟み撃ちにすれば必ず一方はガラ空きです。一対一で対面している仲間が見えたら援護しにいきましょう。自分のキルでなくても、仲間がキルしてくれれば人数アドバンテージがとれます。</p>
<p>仮に仲間がやられても相手をたおすことができれば相手に人数アドバンテージを取られることはありません。手負いの相手をみすみす逃がすようなことのないようにしましょう。</p>
