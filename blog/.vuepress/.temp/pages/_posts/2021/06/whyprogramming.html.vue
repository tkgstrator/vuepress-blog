<p>先程述べたように、B さんの作業効率は A さんの 20 倍です。同じ仕事をさせれば B さんのほうが 20 倍多く仕事量をこなすことができます。じゃあ B さんの給与は A さんの 20 倍かというとそうはなりません。単に同じ給料で B さんの仕事量が 20 倍になるだけです。</p>
