<p>で、残りの 5 時間 48 分は仕事以外のことをします。もちろん「仕事以外のこと」とは「遊ぶこと」ではありません。一応就業時間内は仕事に関係することをしましょう。</p>
<h3 id="作業を更に効率化する" tabindex="-1"><a class="header-anchor" href="#作業を更に効率化する" aria-hidden="true">#</a> 作業を更に効率化する</h3>
<p>ここで大事なのは B さんは<code>SUM()</code>を使える自分に満足して学習することをやめてはいけないということです。</p>
<p>もし、作業するファイルが複数あればそのファイルを開いて<code>SUM()</code>を適用し～ということを繰り返さなければいけません。それをめんどくさいと感じるようになれれば良いです。</p>
<p>つまり「ファイルを開かなくても関数を適用できるような仕組みが荒ればもっと便利なのに」というような「より便利」な仕組み、発想ができるとよいわけですね。なので「残りの 5 時間 42 分でそのような方法を学ぶ」のです。</p>
<p>18 分の作業時間を更に減らすために 5 時間 42 分勉強するのは馬鹿らしい気もしますが、学習とは先行投資なのです。就職するまでに(おそらく高校卒業までは)18 年間学校で学んできたと思いますが、あれもその後の 50 年以上の人生を楽に過ごすための先行投資です。</p>
<p>ここで「今の 18 分の作業時間でも十分満足しているし」となってしまってはダメということですね。</p>
<p>もし仮に更に良いシステムができて作業時間が 5 分になったとすると 1 回の作業で 13 分の差が付きます。5 時間 42 分の学習時間を考えると 27 回作業をするのであれば「トータルの時間的には得」になるわけです。</p>
