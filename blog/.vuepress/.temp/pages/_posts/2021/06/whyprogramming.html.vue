<h2 id="めんどくさがりな人に向いている" tabindex="-1"><a class="header-anchor" href="#めんどくさがりな人に向いている" aria-hidden="true">#</a> めんどくさがりな人に向いている</h2>
<p>なぜプログラミングをするかということなのですが、結局的なところ「人力でやれば一時間かかることを十秒で終わらせたい」からです。</p>
<p>経理などでエクセルに入力されたセルの合計を電卓でちまちま計算している人は流石にいないと思うのですが、例えばセルが 100 行あったとすると、「セルの内容を確認」「電卓に打ち込む」という作業を 1 秒で終えたとしても 100 秒かかります。ダブルチェックしてるとその二倍かかるわけですね。</p>
