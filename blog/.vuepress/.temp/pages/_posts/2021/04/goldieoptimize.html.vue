<p>ウマルートが全 31 ルート中 4 つしか存在しない。じゃあ確率は $\frac{4}{31}$ かと思われるがそうではない。</p>
<p>例えば AG のルートは $\frac{1}{9}×\frac{1}{4}=\frac{1}{36}$ の確率でしか出現しないが、EB のルートは $\frac{1}{9}×\frac{1}{3}=\frac{1}{27}$ の確率で出現する。</p>
<p>これらを考えると、ウマルートを通る確率は、</p>
<p>$\frac{1}{36}+\frac{1}{27}+\frac{1}{27}+\frac{1}{27}=\frac{5}{36}$ となり $\frac{1}{7}$ よりもわずかに小さい。このくらいの確率があれば 7 連続か 8 連続でウマルートを通るシードが見つかる可能性がある。</p>
