<template><h2 id="最適なアタリ位置とは" tabindex="-1"><a class="header-anchor" href="#最適なアタリ位置とは" aria-hidden="true">#</a> 最適なアタリ位置とは</h2>
<p>重要になってくるのが「最適なアタリ位置」という概念である。</p>
<p>少しでもキンシャケ探しに精通しているプレイヤーであれば「コンテナに近ければ良い」と考えるかもしれない。これはある意味で正解であるが、ある意味で間違っている。</p>
<p>例えば、ポラリスにおける最もコンテナに近いアタリ位置は E であるが、満潮時には 12 連続で E がアタリになるシードで 182 納品という記録が達成されている。</p>
<p>しかし、通常潮位の場合は 9 連続で E がアタリになるシードが見つかっているにも関わらず、納品数は 135 にとどまっている。9 連続でキンシャケをたおすことができれば $9×18=162$ 納品は見込めるはずなのだが、なぜこんなに減ってしまったのだろうか？</p>
<p>対してトキシラズいぶし工房では F が最もコンテナに近く、満潮では 12 連続で F がアタリになるシードで 164 納品、通常では 9 連続 F がアタリになるシードで 159 納品が達成されている。</p>
<p>通常と満潮でほとんど納品数に差がないトキシラズと、大きく差がついたポラリス。一体何が違うのだろうか？</p>
<h3 id="重要になるキンシャケルート" tabindex="-1"><a class="header-anchor" href="#重要になるキンシャケルート" aria-hidden="true">#</a> 重要になるキンシャケルート</h3>
<p>実は金イクラ乱獲においてキンシャケのアタリ位置というのはさほど重要ではない。アタリ位置が重要になってくるのはゲーム開始直後と終了間際（最後の一回の位置）だけである。つまり、イベント中に二回だけである。</p>
<p>72 納品以上しようとすれば最低でも四回はキンシャケをたおさなければいけないので、寄与率はたかだか 50%ということになる。</p>
<p>では何が重要になるかというとキンシャケがコンテナに近づいてくるか遠ざかっていくかという「キンシャケが通るルート」である。</p>
<p>どんなに火力の高い編成でもキンシャケを空中でたおし続けることはできない。もちろん、全くイクラを運ばなければクマサン印のチャージャー × 4 の編成なら可能であるが、それでは納品数が伸びない。</p>
<p>金イクラ運び役が必要になる以上、キンシャケをしばらく歩かせて「よりコンテナに近い位置」で金イクラをドロップさせる必要がある。ポラリスで E アタリが通常で納品数が伸びなかったのは、満潮であればコンテナに近づくルートを通るにも関わらず、通常の場合は遠ざかっていくルートをとるためだ。</p>
<p>@<a href="https://www.youtube.com/watch?v=J7nrti31gzA" target="_blank" rel="noopener noreferrer">youtube<OutboundLink/></a></p>
<p>この差はきっとあなたが思っているよりも大きい。というのも、キンシャケが遠ざかっていく場合はキンシャケが降りた瞬間には攻撃するということを強制されるためだ。近づいてくるのであれば近づいてくるまで遠くのイクラを運ぶという別の立ち回りが可能になる。</p>
<p>なので、ポラリス通常においては E が連続してアタリになるのは都合が悪いのである。</p>
<p>キンシャケがコンテナ近くを通り、かつ近づいてくるようなルートを「ウマルート」と呼ぶことにしよう。</p>
