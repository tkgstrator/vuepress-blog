<p>ポラリスは G アタリが最良ではあるものの、少しだけ距離があるため初手は最もコンテナに近い E の方が嬉しい。</p>
<p>即割してしまえばゴール位置よりもアタリ位置のほうが重要になってくるからだ。また、後半もキンシャケを当ててから近づいてくるまでの時間がもったいないので G アタリでは間に合わない可能性が高い。</p>
<p>ここで示したシードはそのような理想のアタリ位置からは少しズレている。</p>
<p>理想となるのは E、E、G、G、G、G、G、G、G、E のようなアタリ位置である。11 回連続でウマルートになるシードもあるが、現実的に 11 回目のキンシャケを出現させるのは時間的に不可能である。</p>
<p>こうなると最初が E が得か、最後が E が得になるかの問題となってくる。
最初に E で時間的に余裕をつくれば後半が G であっても納品する時間があるかもしれないし、最後が E なら初手で G で多少遅れても問題ない気がする。</p>
<p>少なくとも全部 G が 10 回連続するよりは効率が良いだろう。</p>
