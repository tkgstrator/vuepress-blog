<h3 id="注意点" tabindex="-1"><a class="header-anchor" href="#注意点" aria-hidden="true">#</a> 注意点</h3>
<p>自分で修理にするにあたっていくつか問題があるのでそれを最初に書いておきます。</p>
<p>TouchID も交換しないといけない</p>
<p>TouchID は本体の ID と紐ついているので、リペアパーツと交換することができません。</p>
<p>なので、TouchID はぶっ壊れた方の液晶パネルから外して、リペア用の液晶パネルに交換しないといけないわけです。</p>
<p>単純に液晶パネルだけ交換してはいおわり、ではないということ。</p>
<p>カメラ、マイク、スピーカーも交換しないといけない</p>
<p>これらはスピーカー以外はくっついているのでパーツとしては一つだけなのですが、これも交換しないといけません。</p>
<p>そういうのがめんどくさい人は一体型を買うと幸せになれます。</p>
<p>LCD シールドも交換しないといけない</p>
<p>まあこれはやらなくても動作的には問題ないと思うんですが、やっといたほうがいいです。</p>
