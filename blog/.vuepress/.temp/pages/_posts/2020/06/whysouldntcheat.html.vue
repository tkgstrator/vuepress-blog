<p>今回はスプラトゥーンに限って解説していこうと思います。</p>
<p>チートにもいろいろあるが、だいたい人気なのは以下の四つ。</p>
<h3 id="fly" tabindex="-1"><a class="header-anchor" href="#fly" aria-hidden="true">#</a> Fly</h3>
<p>簡単にいえば空が飛べるチート。一時期これを使ってガチマッチで暴れてた God とかいう X パワー 5000 のプレイヤーがいたこともありましたね。コードは普通に公開されていますね。</p>
