<p>その画面で空を飛びながらボム連射をするのが王道チーターのような気がします。</p>
<h2 id="何故チート行為はダメなのか" tabindex="-1"><a class="header-anchor" href="#何故チート行為はダメなのか" aria-hidden="true">#</a> 何故チート行為はダメなのか</h2>
<p>単純な質問のように思えて、ぼくを含めてこれにはっきりと答えられる人はいないのではないでしょうか。</p>
<p>オンラインでのチート行為は法律に違反しているという明確な基準があるため、バカでもダメなことはわかるのですが「じゃあオフラインならいいでしょ？」となるわけです。</p>
<p>実際「オンラインで使用していない」「チート行為の技術・情報の販売をしていない」「金銭を受け取ってチートの導入補助をしていない」などオンラインでチートを使っておらず、かつ金銭の絡みがまったくないケースについては少なくとも日本国内での逮捕例はありません。</p>
<p>記憶違いしてたらスマンメンミ...</p>
<p>ぼくの記憶なので、間違っていたら訂正求みます。</p>
<p>ぼく自身もオフラインであれば自分の好きにすればいいと思いますし、自分が買ったゲーム機なので分解するのも改造するのも自由でいいのではないかと考えています。</p>
<p>が、それを認めた上でやはり「チート行為はダメ」というか「意味のないもの」と思ってしまうのです。</p>
<h3 id="ゲームの三要素" tabindex="-1"><a class="header-anchor" href="#ゲームの三要素" aria-hidden="true">#</a> ゲームの三要素</h3>
<p>Wikipedia によると、エニックス元社員の JUNZO 氏は著書の中で以下のように述べているそうです。</p>
<p>ゲームとは目的を達成する為のルールに基づいた敵との楽しい闘い。</p>
<p>そしてそれを達成するために「目的」「ルール」「敵」がゲームに必要不可欠だと考えたそうです。</p>
<p>ぼくもこの考えに全面的に同意の上、更に根源的な要素として次の三つをゲームの要素と考えました。</p>
<h3 id="ルール" tabindex="-1"><a class="header-anchor" href="#ルール" aria-hidden="true">#</a> ルール</h3>
<p>これはひどく簡単にいってしまえば「ゲームのシステム」と言い換えることができるでしょう。FF でいうならエリクサーを使えば「HP と MP は全回復する」というゲームのシステムが挙げられますし、オセロやリバーシであれば「先手と後手が交互に指す」のを続けて「最後に石が多い方が勝ち」という「勝利条件」ともいえるかもしれません。</p>
<p>もしもオセロやリバーシがマスを全部埋めきってからじゃんけんをして、じゃんけんに勝ったほうが勝利というトンデモルールだったらここまで普及することはなかったでしょう。それなら最初からオセロなんてやらずにじゃんけんしておけばいいのです。</p>
<p>「将棋盤をひっくり返す」とか「ゲームオーバーペナルティを回避するためにゲームの電源を切ってリセットする」というのはゲームのルールを無視する良くない行為なのです。</p>
<h3 id="ゲームクリア" tabindex="-1"><a class="header-anchor" href="#ゲームクリア" aria-hidden="true">#</a> ゲームクリア</h3>
<p>ゲームには終わりが必要です。そうでないとズルズルといつまでも続けることができてしまいます。</p>
<p>その中でもプレイヤー側の勝利で終わる「クリア」は絶対に欠かすことのできない要素です。負けるとわかっているゲームをする人はいないのは当然ですよね。</p>
<p>また「どうすればクリアできるか」というのも先ほどの「ルール」の項目と合わせてはっきりと明記されているべきです。これは JUNZO 氏の定義における「目的」と言えるかもしれません。</p>
<p>一般的な RPG であれば「相手モンスターの HP を 0 にしてバトルに勝利する」というのが短期的な目的で、「ラスボスをたおす」というのが長期的な目的になるわけです。目的がないゲームは羅針盤がない航海と同じようなもので、プレイヤーがプレイ方針を失ってしまいます。</p>
<h3 id="ゲームオーバー" tabindex="-1"><a class="header-anchor" href="#ゲームオーバー" aria-hidden="true">#</a> ゲームオーバー</h3>
<p>意外かもしれませんが、ゲームにはゲームクリアと対極するゲームオーバーが必要です。RPG で「敵モンスターの HP は設定されているが、プレイヤーは無限の HP を持っている」という状況を考えてみてください。こちらはいくら切られようが殴られようが負けないので、こちらの与えるダメージがどんなに小さくても 0 でなければいつかは必ず勝利することができます。</p>
<p>つまり、勝つことがわかっているわけです。ここで、「勝つのがわかっているならバトルする必要はないじゃん」ってなりませんか？エンカウントした瞬間にあいモンスターが降参してクリア報酬のゴールドやアイテムドロップがもらえればいいわけです。</p>
<p>より進めるとそもそもモンスター自体が不要で、歩いていれば一定の確率でゴールドやアイテムが取得できるようなゲームシステムでもいいわけです。こんなゲームが果たして面白いでしょうか？ゲームオーバーがないゲームというのはこんな感じなのです。</p>
<h2 id="チート行為は三要素を破壊する" tabindex="-1"><a class="header-anchor" href="#チート行為は三要素を破壊する" aria-hidden="true">#</a> チート行為は三要素を破壊する</h2>
<p>で、肝心の本題なのですがスプラトゥーンにおけるチート行為はゲームの三要素における「ゲームオーバー」、つまり敗北を絶対にしない状態にしてしまうのです。そしてこれは、チートを使われる側からすれば絶対に「ゲームクリア」ができない状態になるわけです。</p>
<p>また「相手プレイヤーを撃って一定のダメージを与えればたおせる」というゲームのルールをもぶち壊してしまいます。なのでこれらのチートを使ったスプラトゥーンはもはやゲームではありません。</p>
<p>逆にいえばゲームとしての体をなしていないのにチートを使ってプレイし続けることのメリットが全くわかりません。サーモンランで無敵化・ボム連射をして一体何が面白いんでしょう？</p>
<p>絶対勝って報酬がもらえるならセーブエディタでギアとかチケットとか全部 999 にしちゃったほうが時間が節約できていいと思うんですけど。</p>
<p>こういう理由から、スプラトゥーンでチートしちゃう人はまだまだ子供だなあって思っちゃうわけです。</p>
