<h2 id="概要" tabindex="-1"><a class="header-anchor" href="#概要" aria-hidden="true">#</a> 概要</h2>
<p>改修によってホコが取りにくくなり、初動で相手を全落ちさせてもカウントが進みにくくなりました。</p>
<p>その結果、ショッツルホコ特有の初動の駆け引きやスピード感が損なわれはしたものの敵陣を攻めている側が有利なのは変わっていません。</p>
<p>ただ、攻める側が有利と言っても敵陣にホコショットが通るくらいまで近づかないといけないので、それまでは前線がしっかりとホコ持ちを護衛しないと進むことができません。</p>
<p>ステージが広いためにホコ持ちが置き去りにされやすいですが、広いということは中央付近は相手の裏取りも決まりやすいのでホコがどこにあるかは常に確認しておきましょう。</p>
<p>中央のホコが取りにくいことを利用して、水没させてホコを中央に戻してしまうのも戦略の一つとして考えておくと良いかもしれません。</p>
<h2 id="エリア" tabindex="-1"><a class="header-anchor" href="#エリア" aria-hidden="true">#</a> エリア</h2>
<h3 id="危険エリア" tabindex="-1"><a class="header-anchor" href="#危険エリア" aria-hidden="true">#</a> 危険エリア</h3>
<p>ベルトコンベアの間の谷間付近は攻撃が当たりにくい上に相手がホコルートを二通り選択する余地があるため、ホコを取られたときに止めることが困難です。</p>
<h3 id="安全エリア" tabindex="-1"><a class="header-anchor" href="#安全エリア" aria-hidden="true">#</a> 安全エリア</h3>
<p>自陣側は基本的にどこも危ないのでとにかく前へホコを進めましょう。</p>
<p>例外的にこのステージは相手陣に安全エリアがあり、左奥のベルトコンベア付近は自陣ゴールからも相手陣リスポーン地点からも遠いので安全です。</p>
<h3 id="籠城" tabindex="-1"><a class="header-anchor" href="#籠城" aria-hidden="true">#</a> 籠城</h3>
<p>自陣ゴールを経由すれば完全に安全なところに入れることを利用した籠城方法。</p>
<p>20 秒くらい時間が稼げますが、意味もなく籠城しないこと。</p>
<p>失敗すれば相手にホコを取られて一気に逆転されてしまいます。</p>
