<h2 id="概要" tabindex="-1"><a class="header-anchor" href="#概要" aria-hidden="true">#</a> 概要</h2>
<p>ホコを持って前へ進むのが基本戦略。</p>
<p>ノックアウト率が低いにもかかわらず勝率 60%に達するのがカウント 17 なので、「ノックアウトまではいかないもののカウントが進みやすいステージ」といえます。</p>
