<h3 id="緑ルートについて" tabindex="-1"><a class="header-anchor" href="#緑ルートについて" aria-hidden="true">#</a> 緑ルートについて</h3>
<p>緑ルートは無難なのだが、最初カウントを減らすのに時間がかかってしまうのが難点。</p>
<p>上から見られているとなかなかホコを進めにくく、人数アドバンテージをとっていることが条件になる。ただし、ホコ持ちが上がることができれば相手の防衛ラインにホコショットが直通しているので途端に防衛側が苦しくなる。</p>
<p>防衛時は如何にしてホコ持ちを楽にさせないかを考えるステージだといえる。</p>
<div class="custom-container tip"><p class="custom-container-title">緑ルートについて</p>
<p>全く選択肢にないわけではなく、後述する青ルートが見張られていて侵入困難な場合は緑ルートから攻めることになります。</p>
</div>
<h3 id="青ルートについて" tabindex="-1"><a class="header-anchor" href="#青ルートについて" aria-hidden="true">#</a> 青ルートについて</h3>
<p>安定かつ最短ルートなので特に何かしらの条件がついていなければこのルートを選択するのが良い。</p>
<p>実は前に突っ込むだけでカウント 67 までなら減らせるのでとりあえずリードを奪いたいときは前に進めば 67 カウントというのは覚えておくと便利かもしれない。</p>
<p>もし上に進みたいのであれば、ガチホコバリアを割ったときにプロペラが回転して上下に動く床が跳ね上がってしまうので、誰かが床の側面を塗らないといけないことに注意。</p>
