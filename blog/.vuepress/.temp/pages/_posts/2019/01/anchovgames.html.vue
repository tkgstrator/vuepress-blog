<h3 id="赤ルートについて" tabindex="-1"><a class="header-anchor" href="#赤ルートについて" aria-hidden="true">#</a> 赤ルートについて</h3>
<p>何故だいたい負けるかというと、それは赤ルートは相手から見てガチホコ進入禁止エリアになるからです。</p>
<div class="custom-container tip"><p class="custom-container-title">ガチホコ進入禁止エリアについて</p>
<p>ガチホコ進入禁止エリアというのは、防衛側がホコをとってそこに籠城すると攻める側が圧倒的不利になり、ホコ持ち制限上限の 60 秒を消費させることができてしまってゲームが面白くなくなるからあるわけです。</p>
<p>相手にとって圧倒的に有利なエリアを通ってホコを進めようとしているのですがから、無謀だということはわかってもらえると思います。</p>
</div>
