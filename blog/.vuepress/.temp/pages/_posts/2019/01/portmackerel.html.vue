<h2 id="概要" tabindex="-1"><a class="header-anchor" href="#概要" aria-hidden="true">#</a> 概要</h2>
<p>一度押し込まれると打開が困難すぎるためにノックアウト率も高くなっている。</p>
<p>特に、ゴール地点から中央広場にいく場所は相手が隠れるところがありすぎるため、クリアリングをしっかりしないとひたすらやられるだけのデスマシーンになってしまう。</p>
<p>クリアリングにガチホコショットは最適なので、こちらがカウントリードしている状況であれば、自陣ゴール前の高台なりからガチホコショットを繰りかえしてしっかりとクリアリングしつつ、時間を稼ぎたい。</p>
<p>また、平坦で縦に長いステージなのでカーリングボムとハイパープレッサーが恐ろしい威力を発揮する。</p>
<p>相手陣に侵入するための通路は突破が困難だが、自分でカーリングボムを投げた直後にガチホコを取り、その後ろをついていけば簡単にカウントを進めることができてしまう。</p>
<p>また、距離が遠いため、ジャンプビーコンで仲間の前線復帰を早めるとこちらの攻めが続きやすい。</p>
