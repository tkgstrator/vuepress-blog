<p><img src="https://pbs.twimg.com/media/E3GWQ40UUAACUGy?format=png" alt=""></p>
<p>おそらくタチウオパーキングで最も有名なテクニック。</p>
<p>画像内の解説のように、壁を単純に登っているだけだと一番上まで上がって床に足をつくまでカウントが 83 から進まないのだが、途中の金網に足をつけることでそれぞれカウントを 48、44 まで進ませることが出来ます。</p>
<p>超射程スピナーに狙われていたり、上に敵が待ち伏せされていることがわかっている場合には一度足をつけてカウントを確定させてしまったほうが良いです。</p>
