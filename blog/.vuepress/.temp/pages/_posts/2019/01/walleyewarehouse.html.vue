<h2 id="概要" tabindex="-1"><a class="header-anchor" href="#概要" aria-hidden="true">#</a> 概要</h2>
<p>赤ルートは距離が長い上に曲がり角が多いので非常に進みにくい。</p>
<p>時間の無駄なので完全な地雷ルート、絶対に選んではいけない。</p>
<p>無難なのは黄ルートか青ルート。距離的には青ルートが最も近いが、塗れない床を通らなければいけないのでかなり難しい。</p>
<p>黄ルートは塗れない床はないものの距離が長いので青ルートどどっちが良いかは悩みどころ。青ルートは距離が短いので人数有利ですぐに運びたい場合はこれを使うのがいいかも。</p>
<p>どちらにせよ、中央突破が難しいので黄ルートと青ルートの多角攻撃で攻めたいところ。よってノックアウト率も非常に低く、カウントが 10 台まで進むとかなり有利に戦いを進められる。</p>
<h2 id="エリア" tabindex="-1"><a class="header-anchor" href="#エリア" aria-hidden="true">#</a> エリア</h2>
<h3 id="安全エリア" tabindex="-1"><a class="header-anchor" href="#安全エリア" aria-hidden="true">#</a> 安全エリア</h3>
<p>安全エリアが自陣奥深くを経由しないといけない場所が多い。左側の安全エリアはすぐにいけるところにあるが、ガチホコ禁止エリアなのでわざわざ危険を犯してまで持っていく必要がない。</p>
<p>なので自陣側に持ってくるのは得策ではない。そもそもこのステージはガチホコを進めるのが困難なのでガチホコを中央に持っていくこと自体が防衛になる。</p>
<p>防衛時に自陣ゴール前に全員が下に降りてしまうと相手のスペシャルで全滅してしまう可能性があるので、長射程ブキは降りないようにしたい。</p>
<h3 id="危険エリア" tabindex="-1"><a class="header-anchor" href="#危険エリア" aria-hidden="true">#</a> 危険エリア</h3>
<p>特になし。</p>
<p>自陣右通路から運ばれると死角になると高台中央から死角になるのでガチホコを運ばれやすい。左右の通路はしっかりと見ておくこと。特に左通路は相手は選ばないだろうと思いつつも意表をついて選択してくる場合がある。</p>
<p>そのときにジャンプビーコンを設置しておくと索敵機能が働いて裏取りしようとする相手がいるかどうかがわかる。壊されていた場合にもビーコンを壊した誰かがいるはずなので、やはり索敵として機能する。</p>
<h3 id="籠城" tabindex="-1"><a class="header-anchor" href="#籠城" aria-hidden="true">#</a> 籠城</h3>
<p>特になし。</p>
<p>中央に戻してスペシャルを貯めるのが一番の防衛手段である。</p>
<h2 id="小技" tabindex="-1"><a class="header-anchor" href="#小技" aria-hidden="true">#</a> 小技</h2>
<p>アタマこっつんするだけでカウントが 16 から 11 まで進むという不思議な技。11 というカウントは坂道を登りきったカウントと同じなので、塗れない床の坂を登るリスクを犯さずにカウントを進められるのは大きなメリットになる。</p>
<p>最後の塗れない坂道は途中からジャンプすれば短縮できます。が、ジャンプしてもアタマこっつんよりも進まないので一桁狙いたいしか使えません。</p>
