<h3 id="このステージでの考え方" tabindex="-1"><a class="header-anchor" href="#このステージでの考え方" aria-hidden="true">#</a> このステージでの考え方</h3>
<p>まず覚えておいてほしいのは「赤ルートは防衛側が有利なので選択するケースが少ない」ということです。相手もそれをわかっているので、よほどの人数有利が続いていない限り赤ルートを選択しません。</p>
<p>しかしそれは「防衛側が止めようと思えば赤ルートは止めやすい」というだけであって、誰も見ていなければルートの短さ故にあっというまにカウントを進められてしまいます。</p>
<p><img src="https://pbs.twimg.com/media/EW61tKnXsAMkke-?format=png" alt=""></p>
<p>なので「赤ルートは使ってこないだろうけれど、もし使ってきたときにはすぐ止められる」という立ち回りが要求されます。青ルートも同様で、ホコが真ん中付近に落ちているときはいつでも取られて青ルートを進まれることを考えていなければいけません。</p>
<p>例えば中央にホコがある状態で自分がやられたことを考えてみましょう。このとき、まず上部のイカランプを見てどちらがどれだけ人数有利かを瞬時に見極めなければいけません。</p>
<p>もしこのときこちらが人数不利であるなら「相手がホコを持って青ルートを使って進軍してくる」ことを警戒しなければいけません。ホコを持って進んでいる相手を後ろから倒すのは難しいので、人数不利のときは安易に中央へスーパージャンプするのは避けたほうが良いです。スーパージャンプしているスキにカウントを進められてしまう可能性があるからです。</p>
<p><img src="https://pbs.twimg.com/media/EW61vD4X0AYi6O6?format=png" alt=""></p>
<p>また、復帰時に安易にこの場所に降りるのも避けたいところです。ここにいれば赤ルートは止めやすいですが、青ルートを選択されたときに止めるのが難しいです。</p>
<p>ここから降りるときは青ルートを進まれても止められる自信があるときだけにしましょう。</p>
<h3 id="進軍での考え方" tabindex="-1"><a class="header-anchor" href="#進軍での考え方" aria-hidden="true">#</a> 進軍での考え方</h3>
<p><img src="https://pbs.twimg.com/media/EW61zfzWoAAFzZm?format=png" alt=""></p>
<p>青ルートを選択する上で最もホコを止められやすいのがこの壁裏の相手陣地です。何故止められやすいかと言うと、死角が多くて相手がどこにいるのかわかりにくいからです。</p>
<p>なので、ここを突破してホコを前に進められるというのは大きな人数有利があり残りの相手がどこにいるのか把握できている状況（相手の人数が少ないほどどこにいるかはわかりやすい）か、仲間が既にこの陣地を荒らしていて塗りが十分あるかのどちらかです。</p>
<p>相手が見ていない場合は別ですが、見られている場合にはガチホコが先に突っ込んでもただやられるだけなので、ホコ持ちはホコショットで前戦を荒らしている仲間の援護をしてあげると良いでしょう。</p>
