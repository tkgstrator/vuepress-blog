<h3 id="万有引力定数を用いる" tabindex="-1"><a class="header-anchor" href="#万有引力定数を用いる" aria-hidden="true">#</a> 万有引力定数を用いる</h3>
<blockquote>
<p>万有引力定数（ばんゆういんりょくていすう）あるいは（ニュートンの）重力定数（じゅうりょくていすう、英: (Newtonian) constant of gravitation）とは、重力相互作用の大きさを表す物理定数である。アイザック・ニュートンの万有引力の法則において導入された。記号は一般に G で表される。</p>
<p>https://ja.wikipedia.org/wiki/万有引力定数</p>
</blockquote>
<p>万有引力定数とはアイザック・ニュートンが導入した重力相互作用の大きさを表す物理定数である。</p>
<p>物理定数はある特異点を除き全宇宙の全ての場所で等しい値を持ちます。場所が火星だろうと、木星だろうと、太陽だろうと同じです。当然、12000 年後の地球でも値は変わりません。</p>
<p>「え、でもそれって現実の話でしょ...？今はスプラトゥーンの話してるのに...ゲームと現実の区別がつかないなんて犯罪者予備軍？？」とか思ったそこのあなた！！それは大いに間違っています。</p>
<h3 id="物理エンジン" tabindex="-1"><a class="header-anchor" href="#物理エンジン" aria-hidden="true">#</a> 物理エンジン</h3>
<p>というのも、FPS や TPS などでは最近はリアリティを重視して物理エンジンを搭載しているのがもはや常識になっているからです。</p>
<p>例えば、ジャンプ一つにしても今までは何フレーム目にキャラクターをこれだけ上昇させてーとかいちいちプログラマが調整していたわけです。「このキャラクターもうちょっとジャンプ力あげよう」だなんて言われた日にはまたパラメータを打ち直す作業が必要だったわけですが、今は物理エンジンのおかげでキャラクターが飛び上がる強さだけを入力すればあとは勝手に物理エンジンが t 秒後のキャラクターの位置を計算してくれます。</p>
<p>これは、ジャンプだけにとどまらず弾丸の落下や物体の投擲、光の反射など応用場面は数え切れません。</p>
<p>もはやゲームは現実世界の投影になりつつあるのです。</p>
<h3 id="イカちゃんのジャンプに注目する" tabindex="-1"><a class="header-anchor" href="#イカちゃんのジャンプに注目する" aria-hidden="true">#</a> イカちゃんのジャンプに注目する</h3>
<p>本論の主張はこうである。</p>
<p>万有引力定数は 12000 年後も変わっていないはずである。</p>
<p>であれば、イカちゃんたちが住んでいるのが地球であれば重力加速度も容易に求められます。</p>
<p>すると例えば初速 0 から落下に t 秒かかったということがわかれば、物体がどのくらいの高さにあったか、小数点以下 5 桁ほどの精度で計算できます。</p>
<p>つまり、調べるのはイカちゃんがジャンプして再び地面に足をつくまでの時間なのです。これならば複雑な計算を必要とせずに計測できますね！！</p>
<p><img src="https://pbs.twimg.com/media/E348LssVoAAMmMm?format=png" alt=""></p>
<p>1 フレーム目。</p>
<p>最高到達点を調べるのだが、足は若干折り曲げた感じになっているので測定にふさわしくない。そこで相対的な位置が変わらない腰の位置に注目し、最高到達点に達するまでのフレーム数を調べた。</p>
<p>すると、およそ 30 フレーム目付近で最も高く飛び上がっていることがわかった。</p>
<p>1 秒間に 60 フレーム録画しているので、30 フレームとは 0.5 秒に相当する。既存の物理法則が正しく働いているなら上昇にかかる時間と落下にかかる時間は同じはずなのだが、さてどうなっているか...</p>
<p>すると 57 フレーム目付近でイカちゃんの足が地面についていることが確認できた。正確に半分、とはなってはいないがジャンプの仕方や足の付き方などでこの辺りはいくらでも誤差が生まれるものである。</p>
<p>システム的にも通常のジャンプは 60 フレーム飛べると考えるのが一般的だろう。</p>
<p>重力によって物体が落下する場合、その落下時間はイカの式で求めることができる。</p>
<p>$s=\frac{1}{2}gt^2$</p>
<p>今回の場合、イカちゃんが落下するのにかかる時間は 0.5 秒であるので、</p>
<p>$s=\frac{1}{2}×9.80665×(0.5)^2=1.22583125$</p>
<p>計算上はインクリングの垂直跳びの記録はおよそ 123cm であることがわかります。成人男性の平均が 55.2cm 程度らしいので、イカちゃんはめっちゃジャンプ力が強いということに...</p>
<p>イカすアートブックによると「イカ足はひと跳び 150cm」とあるので、真面目にやれば 150cm は跳べるらしい。このイカちゃんはどう見ても軽くこなしているので 8 割くらいの力を出したとしたら 120cm 前後でも決しておかしくない数字になります。</p>
