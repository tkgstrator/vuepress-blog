<h3 id="地下ドームの設定を活かす" tabindex="-1"><a class="header-anchor" href="#地下ドームの設定を活かす" aria-hidden="true">#</a> 地下ドームの設定を活かす</h3>
<p>要はイカちゃんが高くジャンプできればいいわけです。月は地球の重力のおよそ $\frac{1}{6}$ なので、月に行けば走り幅跳びの記録は地球記録の 6 倍くらいでることになります。</p>
<p>イカちゃんの場合は 123cm が 150cm になればいいだけなので 6 倍も変化する必要はありません。</p>
<p>重力加速度がちょっと弱くなればいいのでは？</p>
<p>オクタリアンが生活している地下ドームは人類が建造した最後の大型建築物です。当然、地下を掘れば土が出ます。</p>
<p>想像もつかないほど巨大な地下ドームを作ればその土をどこにやるかという問題が生じます。地上は残り少ない、海に捨てれば余計に海面上昇が起こる...</p>
<p>そうだ、宇宙に捨てよう！！！</p>
<p>重力加速度は次の式で求められます。この式によれば地球の質量 M が小さくなれば重力加速度も小さくなります。</p>
<p>$g=\frac{GM}{R^2}$</p>
<p>そもそも、1 秒の滞空時間で 150cm の垂直跳びの記録を出すために必要な重力加速度はいくらなのでしょうか？</p>
<p>$g=1.5×2^2×2=12.0$</p>
<p>あれれ、0.5 秒の滞空時間で 150cm 飛ぼうとしたら重力が今よりも強くならなければいけないことになってしまいました。</p>
<p>よくよく考えたらこれは当然で、今の地球だと 0.5 秒ではどう頑張っても 123cm しか落下できないわけです。そこを 150cm 落下しようとしたら今よりも重力が強くないといけないわけです。</p>
<p>え、じゃあ土を宇宙に捨てちゃダメじゃん。</p>
<h3 id="msi-単位系が変わった" tabindex="-1"><a class="header-anchor" href="#msi-単位系が変わった" aria-hidden="true">#</a> MSI 単位系が変わった</h3>
<p>ゴリ押しの方法である。</p>
<p>どうやってもイカちゃんの身長が 150cm を超えないのであれば、MSI 単位系が変わったことにすれば良い。</p>
<p>貨幣のデノミネーションみたいな感じで「今日の 120cm は明日の 150cm」みたいな感じにすればよいのである。これなら実際にイカちゃんの身長は 150cm になるし、物理エンジンを利用した身長の計算方法とも矛盾しないわけである。</p>
<h3 id="実はこれもダメ" tabindex="-1"><a class="header-anchor" href="#実はこれもダメ" aria-hidden="true">#</a> 実はこれもダメ</h3>
<p>何故なら 1m は厳密に光速度から定義されているため。</p>
<blockquote>
<p>1983年（昭和58年）に基準が見直され、現在は1秒の 299792458 分の1の時間に光が真空中を伝わる長さとして定義されている。</p>
<p>https://ja.wikipedia.org/wiki/メートル</p>
</blockquote>
<p>真空中の光速度は相対性理論によると不変のはずなので、もちろん 12000 年後も変わっていない。</p>
<p>「299792458 分の 1 秒～」のところを変えれば 1m の定義も変わるのだが、わざわざ変える意味がない。享楽的で単細胞なインクリングであれば人類が残した MSI 単位系を面倒くさい作業をして変えるとは考えにくい。</p>
<h3 id="ヒメちゃんがサバを読んでいる" tabindex="-1"><a class="header-anchor" href="#ヒメちゃんがサバを読んでいる" aria-hidden="true">#</a> ヒメちゃんがサバを読んでいる</h3>
<p>意外とこれが一番ありそうだったりして。</p>
<p>しかし、インクリングが 123cm というのが正しいのであればヒメちゃんは 20cm 近くもサバを読んでいることになる。</p>
<p>スポーツ選手や格闘家が相手に舐められないように 1 ~ 2cm 身長を高く申告したりする場合があるようだが、流石に 20cm もサバを読んだらあっという間にバレるのではないだろうか？</p>
<p>いや、でもそこは享楽的なインクリングは「ヒメちゃんなんか身長小さいな」くらいで済ましてしまうかもしれない...</p>
<h2 id="まとめ" tabindex="-1"><a class="header-anchor" href="#まとめ" aria-hidden="true">#</a> まとめ</h2>
<p>物理エンジンが正しいと仮定した場合、イカちゃんの身長は 120 ~ 130cm 前後であることがわかりました（他のインクリングをジャンプできないことからも 130cm の方が近いと思われる）</p>
<p>しかし、ヒメ先輩と身長を比較すると明らかに自称 145cm のヒメ先輩のほうが身長が低く、130cm というのは考えにくいです。</p>
<p>逆に「ひと跳び 150cm」を根拠に他のインクリングをジャンプで越えられないことから 155cm くらいと考えたほうがまだ納得がいきます。</p>
<p>というわけで、今回の検証からはインクリングの身長に関しては「130cm 前後でヒメちゃんがサバを読んでいる」か「155cm 前後で物理法則を含む世界の法則が乱れた（もしくは物理エンジンが正確なものをわざと使用していない）」という結論が得られました。</p>
<p>少なくとも身長が 1m イカだとか、そういうことはなさそうです。</p>
