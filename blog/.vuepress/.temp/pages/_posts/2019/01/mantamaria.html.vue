<h2 id="概要" tabindex="-1"><a class="header-anchor" href="#概要" aria-hidden="true">#</a> 概要</h2>
<p>赤ルートが最も手っ取り早くカウントを 40 台まで減らせるものの、ジャンプするところが難しいので相手に見られているとまず突破は望めない。また、ジャンプした先で倒されるとガチホコバリアが邪魔で右から裏取りするルートが潰れてしまい、中央からしか相手を倒しにいけなくなってしまうのもデメリット。</p>
<p>ただ、見られていなければカウントをグッと進められるのでイケると思ったときには選択しても良いかも。</p>
<p>緑ルートは距離が長いのがネックだが、チームとしてキル勝ちしているのであれば最も安全にカウントを減らすことができる。ただ、選択するプレイヤーがあまりいない印象があるので、仲間の動きと相談して選択すること。</p>
<p>中央の青ルートが基本的なルートとなるが、ここもやはり相手が減っていないと進軍は難しい。結局、どのルートも進軍が難しいため、ノックアウト率も低い。</p>
<p>カウント 46 のポイントからはガチホコショットフルチャージでゴールまで塗れる上に、下に落ちるだけでカウントが 5 も進むので、一発ガチホコショットを撃った後は（ノックアウトが難しいこのステージならではなのだが）やられる覚悟で突っ込んだほうが良いのかもしれない。</p>
<h2 id="エリア" tabindex="-1"><a class="header-anchor" href="#エリア" aria-hidden="true">#</a> エリア</h2>
<h3 id="安全エリア" tabindex="-1"><a class="header-anchor" href="#安全エリア" aria-hidden="true">#</a> 安全エリア</h3>
<p>自陣高台右が安全エリアになる。ここに登るには迂回してくるか目の前の壁を塗って侵入するしかないが、ガチホコショットで警戒していればそれも容易ではない。</p>
<p>ただし、ここでやられてしまうとガチホコを拾われたときに一気にカウントを進められてしまうのでやられるときは必ず下に落ちてからやられること。またはステージ外に落として中央に戻してしまっても良い。</p>
<h3 id="危険エリア" tabindex="-1"><a class="header-anchor" href="#危険エリア" aria-hidden="true">#</a> 危険エリア</h3>
<p>自陣ゴール前および通路はホコを放置するとかなり危ないです。スペシャルがささるとノックアウトされる可能性まである。右側ルートを使って戻すか、左からガチホコをステージ外に落として中央に戻すこと。</p>
<p>相手がまだいるのに安易に中央通路を使ってガチホコを戻そうとすると再びやられてホコを落とす可能性があるので注意。</p>
<h3 id="籠城" tabindex="-1"><a class="header-anchor" href="#籠城" aria-hidden="true">#</a> 籠城</h3>
<p>ここに相手が来るのはほとんど不可能なので勝ちが確定する。</p>
<p>残り 20 秒を切ったら実行しても問題ないが、坂が塗れていないとここにくるまでに時間がかかるので塗り状況は必ず確認してから行うこと。</p>
<p>絶対安全地点であるにもかかわらず、相手から目視可能な場所がいくつかあるのでひょっこり顔を出していればガチホコ持ちを倒してくれてそれこそ完封できてしまうかもしれない...</p>
