<h2 id="概要" tabindex="-1"><a class="header-anchor" href="#概要" aria-hidden="true">#</a> 概要</h2>
<p>ガチホコを運ぶルートは二つあるが、一見遠回りに見える青ルートが安定ルート。</p>
<p>塗れない床がないので進みやすく、カーブしているので相手の攻撃が当たりにくいのがポイント。</p>
<p>ガチホコショットであれば放物線を描いて置くまで攻撃できるのも強み。</p>
<p>と、基本的にはここのカーブはガチホコショットが強いのだが、それよりも優先されるのはホコ持ちがガンガン前に進むということである。</p>
<p>人数差がないのであれば進軍側も互角以上に戦える上に、防衛側は黄色ルートから裏取り強襲を試みるとすぐに青ルート上に戻れない地形になっているためである。</p>
<p>しかし、裏取りがや高所からの防衛が間に合ってしまうと簡単にホコは止められてしまいます。</p>
<p>つまり、このステージは有利だと思ったらやられてもいいのでガンガン前に進むことが大切なのです。</p>
<p>押し込まれていて、ガチホコを中央に戻したい場合は自陣左側を経由するのがホコルートから遠いのでおすすめ。</p>
<p>下に落ちればここは時計回りに回転するギミックがあるので、ガチホコが自陣からだんだん離れていくため比較的安全です。</p>
