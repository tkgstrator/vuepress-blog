<h2 id="概要" tabindex="-1"><a class="header-anchor" href="#概要" aria-hidden="true">#</a> 概要</h2>
<p>リスポーン地点を横切るようにガチホコルートがあるため、相手を三落ち以上にできたら簡単にノックアウトできるステージ。特に緑ルートはリスポーン地点からの応援が全くきかないので強い。</p>
<p>青ルートは初動で割り勝って相手が二落ち以上になったときに選択するとあっという間に 20 くらいまで進められるので、速さ重視のルートと言える。</p>
<p>相手が減っていないと進めるのは難しいが、その場合に仲間が緑ルートを使って裏取りに成功すると挟み撃ちで一気にノックアウトできる。正面だけの攻めでは膠着してしまうので、やはりこのステージも多角的に攻めること。</p>
<p>緑ルートは箱にのって敵陣側にジャンプする前にやられてしまうとカウンターされかねないので注意。しかし、相手のリスポーン地点から遠いので大逆転勝ちや大幅リードを奪いやすく、狙えるならば積極的に狙っていきたい。</p>
