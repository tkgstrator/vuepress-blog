<h2 id="概要" tabindex="-1"><a class="header-anchor" href="#概要" aria-hidden="true">#</a> 概要</h2>
<p>閉鎖されていた B バスホコが改修されて帰ってきました。縦にものすごく長くなったことによって即落ちから大逆転でノックアウトという理不尽さは減りました。</p>
<p>ホコルートは大まかに分けて四つですが、このステージは柔軟な選択が要求されます。</p>
<p>というのも、最も近いのは青ルートなのですが、これはあまりにも見晴らしがいいので相手に長射程ブキがいた場合にあっという間にやられてしまうからです。つまり、正面から突破するのは相手の長射程がいないときに限られます。</p>
<p>左赤ルートは迂回中にやられると自陣ゴールに近くなるのと、迂回したとしてもカウントが 1 も減らないので不採用です。それに比べると右赤ルートはまだマシですが、ここを使うくらいなら青ルートでいいような気がします。</p>
<p>緑ルートは奇襲ルートで、相手インクに染まっていても手すりの上を歩くことでカウントを一人で進めることができます。</p>
