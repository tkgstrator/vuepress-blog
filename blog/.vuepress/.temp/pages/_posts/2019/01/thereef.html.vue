<h2 id="概要" tabindex="-1"><a class="header-anchor" href="#概要" aria-hidden="true">#</a> 概要</h2>
<p>もともとガチアサリの防衛側が圧倒的不利という欠陥を除けばそれなりに良ステージだったにもかかわらず何故か改修されたという謎ステージ。</p>
<p>しかも大幅に変更を加えられたため、一から解説記事を書き直すことにしました。まず、ガチホコルートは三つあるのですが、旧ステージの最強ルートだった左ルートは改修で死にました。</p>
<p>では、なぜ左ルートがダメかを解説します。まず、一つ目の理由は単純に距離が長いというのがあります。当然距離が短いほうがカウントが進むのが早いので、わざわざ長いルートを選ぶ必要性はありません。</p>
<blockquote>
<p>実際にはゴールまでの離が長くても例えば相手が防衛するのが難しかったり、相手のリスポーン地点から遠く進軍側が優位に戦えるのであれば選択する価値があります。</p>
</blockquote>
<p>二つ目の理由は、このルートは選択したことが相手にバレバレだということです。こっそり運べればいいのですが、このルートだけ他のルートと全く違うところを通るので簡単にバレて先回りされてしまいます。また、途中でルートを変更することもできないので柔軟に進軍をすることができません。</p>
<p>三つ目の理由は一本道で逃げる場所がないということです。改修前は防衛側も狭く、ガチホコショットがあるので進軍側が優位に戦えていましたが、改修後は防衛側だけが広くなったので進軍側が不利な地形になってしまいました。</p>
<p>以上の理由から、赤ルートを選択する理由はまったくないことがわかります。</p>
<p>青ルートは最短ルートですが、最後に塗れない床があるので緑ルートとどちらを選択するか悩むところです。</p>
<p><img src="https://pbs.twimg.com/media/E3GW3XPUUAQWkL1?format=png" alt=""></p>
<p>緑ルートは相手からの死角になっているので攻撃を受けにくく、青ルートよりも進む場合があるので臨機応変に動けるようにしたいところ。</p>
