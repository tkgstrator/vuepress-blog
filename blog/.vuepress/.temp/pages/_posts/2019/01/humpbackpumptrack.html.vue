<h2 id="概要" tabindex="-1"><a class="header-anchor" href="#概要" aria-hidden="true">#</a> 概要</h2>
<p>コンブトラックの安定ルートは右側から攻めるルートである。ここは敵が待ち伏せしやすく、進めにくいルートであるのだが、それ以上に相手に進軍されにくいためにオススメしやすい。</p>
<p>左ルートは確かに距離的には一番短いのだが、選択していることが相手にバレバレなのでちゃんと見られていると簡単に止められます。</p>
<p>また、ここでガチホコを落とした場合に相手はそのまま直進してブロックを越えることで安定ルートを選択できるので非常にカウンターを受けやすいです。カウントリードできている場合には使わないほうがいいでしょう</p>
<h2 id="エリア" tabindex="-1"><a class="header-anchor" href="#エリア" aria-hidden="true">#</a> エリア</h2>
<h3 id="安全エリア" tabindex="-1"><a class="header-anchor" href="#安全エリア" aria-hidden="true">#</a> 安全エリア</h3>
<p>自陣右ブロック付近が安全エリアとなる。</p>
<p>ここは前に降りれば一気にゴールまでの距離が伸びるため、ちゃんと使うことができれば非常に安全なポイントである。</p>
<p>特に、（こちらがリードしている状況で）防衛に成功した場合は馬鹿正直に左から戻すのではなく、中央を経由してでもこの安全エリアに戻したほうが良い場合がある。こちらの方が再度相手に攻められたときに距離が長く、止められる確率が高いからである。</p>
<h3 id="危険エリア" tabindex="-1"><a class="header-anchor" href="#危険エリア" aria-hidden="true">#</a> 危険エリア</h3>
<p>自陣左にガチホコを持っていってはいけない。ここはガチホコを奪われた場合に、相手がすぐに進軍ルートを選択することができるからである。</p>
<h3 id="籠城" tabindex="-1"><a class="header-anchor" href="#籠城" aria-hidden="true">#</a> 籠城</h3>
<p>コンブトラックは自陣までスーパージャンプなしに戻ることができないので、完全な籠城ポイントは存在しない。</p>
