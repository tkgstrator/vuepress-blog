<h2 id="概要" tabindex="-1"><a class="header-anchor" href="#概要" aria-hidden="true">#</a> 概要</h2>
<p>スプラトゥーン 2 の全 23 ステージの中で唯一の線対称ステージである。</p>
<p>ステージ後方にガチマッチとは全く関係ない謎の領域が広がっており、ここをどうやって使うかが試合のカギとなる場面も多そうだ。</p>
<p>ホコルート自体は四つあるものの、現実的に使うとなると赤以外の三つになりそうだ。というのも、赤と青はほとんど同じルートであるにもかかわらずカウントを 32 から減らすために通らなければいけない距離が全く違うためである。</p>
<p>赤ルートは常に前方から相手が向かってくるのに対して、青ルートは段差があるために相手の攻撃が当たりにくいというメリットがある。段差下からジェットパックでのぼり、段差上の敵にプレッシャーを掛けることができればカウントを大きく進めることができる。</p>
<p>ピンクのルートは中央からガチホコを運ぶためのルートというよりは、相手に進められたホコを中央を経由せずに相手ゴールまで近づけるためのルートだ。</p>
<p><img src="https://pbs.twimg.com/media/E3GWMQYUUAIFYBj?format=png" alt=""></p>
<p><img src="https://pbs.twimg.com/media/E3GWN3XUcAMRNu-?format=png" alt=""></p>
<p>見てわかるように、ジャンプするだけでカウントを 60 近くも進めることができる。ここは塗れない床なので前線が押し込めていないとここから先に進めるのは困難だが、カウント 40 でリードを奪えるような状況であれば一発逆転を狙ってみるのもアリだろう。</p>
<p>ただし、他のルートでカウントを進められそうなときにわざわざこのルートを選択しないこと。</p>
