<h2 id="概要" tabindex="-1"><a class="header-anchor" href="#概要" aria-hidden="true">#</a> 概要</h2>
<p>一度自陣ゴール前に相手の侵入を許すとリスキル状態になってしまうのでそれだけは絶対にさせないこと。</p>
<p>特にビーコンが設置されている場合は最優先で潰さなくてはいけない。</p>
<p>赤ルートは最短ルートであるものの、相手のカウンターがきやすいので初動で相手の意表をつくとき以外は選択してはいけない。網上でガチホコを落としてしまった場合には攻めの継続ができないからだ。特にこちらがカウントリードしているときにこのルートを絶対に選択してはいけない。</p>
<p>初動で相手が網側を見ていないことをマップで確認してからこのルートを選択したい。このルートからくるのは長射程ブキであるスピナーやチャージャーであることが多いので、それらのブキが相手にいるかどうかを確認しているだけでかなり奇襲の成功率が違ってくる。</p>
<p><video controls src="https://video.twimg.com/ext_tw_video/996295626617847809/pu/vid/1280x720/zAsrVVUto3UuTX4i.mp4"></video></p>
<p>安定ルートを使う際の最後の直線はバブルを置いておけば中を通って簡単に 30 台までは進めてしまう。また、ハイパープレッサーもささるので防衛側は相手のスペシャルがたまっていると苦しい戦いを強いられることになる。</p>
<p>この通路は防衛側からすると非常に戦いにくい場所なので、さっさと自陣リスポーン前を通って安全エリアまでガチホコを運んでしまおう。</p>
<h2 id="エリア" tabindex="-1"><a class="header-anchor" href="#エリア" aria-hidden="true">#</a> エリア</h2>
<h3 id="安全エリア" tabindex="-1"><a class="header-anchor" href="#安全エリア" aria-hidden="true">#</a> 安全エリア</h3>
<p>自陣右側は相手がリスポーン地点前を通るか、網上を歩いてくるしか辿り着く方法がないので基本的に安全。そこから敵陣に向けてガチホコショットを撃ちながら態勢を整えつつ、仲間が網上を見ておけば相手がガチホコを奪いに来るのは容易ではない。</p>
<p>危なくなったら下に落ちれば、相手の安定ルートから最も遠いので安全である。</p>
<h3 id="危険エリア" tabindex="-1"><a class="header-anchor" href="#危険エリア" aria-hidden="true">#</a> 危険エリア</h3>
<p>自陣左側は相手の高台から応援がいくらでも利くので正面と左から同時に責められることになり常に危険。左側からガチホコを戻すことは避け、自陣ゴール前を経由して籠城ポイントを通ってから安全エリアに戻すこと。</p>
<h3 id="籠城" tabindex="-1"><a class="header-anchor" href="#籠城" aria-hidden="true">#</a> 籠城</h3>
<p>安全に籠城できる場所はないが、時間を稼ぐだけであれば自陣右側高台が有効。ただし、ここでやられることは絶対に避けること。</p>
