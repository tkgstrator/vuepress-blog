<p>ｒ相手の最後の進軍時にこのあたりにホコをおいておけばほとんど勝ちは決まったようなものである。</p>
<h3 id="危険エリア" tabindex="-1"><a class="header-anchor" href="#危険エリア" aria-hidden="true">#</a> 危険エリア</h3>
<p>ステージ右側にあるガチホコは常に危なく、いつでもカウント 40 台まで進んでしまう可能性がある。</p>
<p>相手がホコをとっている場合にはホコショットが極めて通りやすく、防衛がしにくいのでむやみに突っ込むのは絶対にやめたい。</p>
<p>スペシャルやボムでけん制して相手がしびれを切らすのを待つような戦術が必要とされる。</p>
