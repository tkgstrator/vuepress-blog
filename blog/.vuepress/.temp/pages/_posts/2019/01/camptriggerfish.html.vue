<h2 id="概要" tabindex="-1"><a class="header-anchor" href="#概要" aria-hidden="true">#</a> 概要</h2>
<p>ルートが二つしかなく、選択肢が少ないが開幕は中央のホコを割りにいく以外の選択肢はないといってよい。</p>
<p>何故か右から強襲をかけたがるイカが多いが（特にボールドとローラー）全くの無意味である。</p>
<ul>
<li>右ルートの裏取りは単純に時間がかかる</li>
<li>裏取りしている間にホコバリアを割られてしまう</li>
<li>以下、バリアを割られた時の状況を想定する
<ul>
<li>相手が左ルートを選択したとき
<ul>
<li>相手の進軍と鉢合わせになる</li>
<li>やられる可能性が高い</li>
<li>やられたときにそのまま一気にカウントを進められる</li>
</ul>
</li>
<li>相手が中央ルートを選択したとき
<ul>
<li>防衛に全く参加できない</li>
</ul>
</li>
</ul>
</li>
</ul>
<p>というわけで、初動はホコ割参加以外に考えられない。</p>
<p>むしろ相手がホコ割を無視して裏取りしているのが見えたらさっさとバリアを割って中央突破すればいい。</p>
<p>また、ステージが広いのでどこにホコがあるのかは必ず確認すること。</p>
<p>ホコショットが通りやすいのは左ルートで、相手からも攻めにくい安定ルートだが距離が長いため時間がかかってしまうのが難点。</p>
<p>開幕でホコを割り勝って二落ち以上であればスピードがある中央ルートも十分選択としてはアリだろう。中央ルートはホコより前に誰かがいってあげないと簡単に止められてしまうので、ホコ持ちを先導する動きをしたいところである。</p>
<p>とにかく中央ルートは速度が大事なので、さっさと前に突っ込もう！</p>
<p>左ルートはどうしてもイカ状態になれない金網を通らなければいけないのでヒト速度アップのギアを積んでおきたい。</p>
<p>また、左ルートはじわじわと押していくルートなのでホコ持ちはあんまりガンガン攻め込まないほうがいいこともある。金網上の仲間のアシストや相手のインクレール方向を撃って応援が来れないようにするといった動きを心掛けたい。</p>
